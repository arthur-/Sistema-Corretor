<h1><strong>L2Q2</strong> - Isso é uma árvore?</h1>
<ul class="lista_menu">
	<li>Código fonte: <strong>L2Q2.c</strong>, <strong>L2Q2.cpp</strong> ou <strong>L2Q2.java</strong></li>
	<li>Entrada: <strong>L2Q2.in</strong></li>
	<li>Saída: <strong>L2Q2.out</strong></li>
	
	<li style="background: #2A2; top: -28px; float: right;"><strong>Revisão</strong></li>
	<li style="background: #A22; top: -28px; float: right;"><strong>Clarifications</strong></li>
</ul>
<pre>Alice e Bob estavam discutindo sobre uma forma eficiente de determinar se uma rede de nós e arestas pode ser considerado uma árvore ou não. Quando uma maçã caiu na cabeça de Bob, ele teve uma idéia genial, mas decidiu não contar a Alice sua solução. Agora, Alice precisa de sua ajuda, já que ela não teve a mesma sorte que Bob.
</pre>

<h2>Formato de Entrada</h2>
<pre>No arquivo de entrada você vai receber um inteiro C informando o número de casos na entrada. Cada caso vai ter 2 inteiros, N e M que correspondem ao número de nós e a quantidade de arestas, respectivamente. Nas próximas M linhas, você terá dois inteiros A e B que representa uma aresta indo de A para B.
</pre>

<h2>Formato de Saída</h2>
<pre>Para cada caso de teste, imprima "Caso #D: R", onde D é o número do caso (começando em 1) e R será 1 se o grafo da entrada puder ser uma árvore ou 0 se não puder.
</pre>

